package it.vrscuola.scuola.models;

public class ModaleCardVisoreModel {
    private String href;
    private String title;

    public ModaleCardVisoreModel(String href, String title) {
        this.href = href;
        this.title = title;
    }

    public String getHref() {
        return href;
    }

    public String getTitle() {
        return title;
    }


}
